let $translations = {};

const locales = Object.keys($allTranslations);
const $locale = localStorage.getItem("lang") && locales.includes(localStorage.getItem("lang")) ? localStorage.getItem("lang") : "fr";

const switchLocale = () => {
  const nextLocale = $locale === "fr" ? "en" : "fr";
  localStorage.setItem("lang", locales.includes(nextLocale) ? nextLocale : $locale);
}

const parseTrans = (_object, _path = '') => {
  Object.entries(_object).forEach(([_key, _value]) => {
    let fullPath = _path ? [_path, _key].join('.') : _key;
    if (typeof _value === 'string') {
      $translations[fullPath] = _value;
    } else {
      parseTrans(_value, fullPath);
    }
  });
}
parseTrans($allTranslations[$locale]);

const $trans = (_id, _params = {}) => {
  let text = $translations[_id];
  if (text) {
    Object.entries(_params).forEach(([_key, _value]) => {
      text = text.replace(`{${_key}}`, _value);
    });

    return text;
  }

  return '';
}

document.documentElement.lang = $locale;
document.title = $trans("title");
document.querySelector("meta[name='description']").setAttribute("content", $trans("description"));